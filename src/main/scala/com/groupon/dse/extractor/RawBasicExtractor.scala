package com.groupon.dse.extractor

import com.groupon.dse.kafka.common.WrappedMessage
import org.apache.spark.rdd.RDD

/**
 * An Extractor that will convert the payload in each [[WrappedMessage]] object into a String
 */
class RawBasicExtractor extends Extractor {
  override def extract(messages: RDD[WrappedMessage]): RDD[TopicAndEvent] = {
    messages.map(m => TopicAndEvent(m.topic, new String(m.payload)))
  }
}
